
# Given array number of integers return all 3Sums that are equal to zero
# input: array of integers
# output: all three indexes of the array that add up to zero

from typing import List;

class ThreeSumUp:
    def three_sum_indexes(self, nums: List[int]) -> List[List[int]]:
        # This can be looked at as finding a third element to the twoSum problem
        # We solve the twoSum problem in O(n) time and space complexity and can solve this in O(n^2) time and O(n) space.

        # in each iteration we send the negative of an element as target
        three_sum = []
        for index, num in enumerate(nums):
            sliced_nums = nums[:index] + nums[index + 1:]
            target = -num
            # O(n) time and space
            two_sum = self.twoSum(sliced_nums, target)
            for item in two_sum:
                value = self.shift_numbers(item, target)
                three_sum.append(value + [target])

        return three_sum

    def two_sum_indexes(self, nums: List[int], target) -> List[List[int]]:

        # going through each index and add the needed
        # int to get to the target to a hashmap
        target_hashmap = {}

        # time: O(n)
        # space: O(n)
        for index, num in enumerate(nums):
            target_lookup = target - num
            value = target_hashmap.get(target_lookup)
            if value is None:
                target_hashmap[target_lookup] = [index]
            else:
                target_hashmap[target_lookup].append(index)

        # go through each number again to find if it matches with other indexes
        two_sum_array = []

        # time: O(n)
        # space: O(n)
        # this might look like an O(n^2) but its actually on O(n). Since each target can only get mapped to one number.
        for index, num in enumerate(nums):
            target_indexes = target_hashmap.get(num)
            if target_indexes is not None:
                for target_index in target_indexes:
                    if not target_index <= index:
                        two_sum_array.append([index, target_index])

        return two_sum_array

    def shift_numbers(self, items, target) -> List[int]:
        for index, item in enumerate(items):
            if item >= target:
                items[index] += 1
        return items


    def three_sum_values(self, nums: List[int]) -> List[List[int]]:
        entered_hash = {}
        three_sum = []

        for index, num in enumerate(nums):
            slices_nums = nums[:index] + nums[index + 1:]
            target = -num
            two_sum = self.two_sum_values(slices_nums, target)
            for pair in two_sum:
                sorted_triple = sorted(pair + [num])
                str_triple = ''.join([str(x) for x in sorted_triple])
                value = entered_hash.get(str_triple)
                if value:
                    continue
                entered_hash[str_triple] = True
                three_sum.append(sorted_triple)
        return three_sum

    def two_sum_values(self, nums: List[int], target) -> List[List[int]]:
        target_hashmap = {}

        # time: O(n)
        # space: O(n)
        for index, num in enumerate(nums):
            target_lookup = target - num
            value = target_hashmap.get(target_lookup)
            if value == None:
                target_hashmap[target_lookup] = 0
            else:
                target_hashmap[target_lookup] += 1

        # go through each number again to find if it matches with other indexes
        two_sum_array = []
        entered_hash = {}

        # time: O(n)
        # space: O(n)
        for num in nums:
            value = target_hashmap.get(num)
            if value == None:
                continue
            if num == target/2:
                if value == 0 or value is None:
                    continue
            val1 = min(num, target - num)
            val2 = max(num, target - num)
            if entered_hash.get(str(val1) + str(val2)):
                continue
            entered_hash[str(val1) + str(val2)] = True
            two_sum_array.append([num, target - num])

        return two_sum_array


